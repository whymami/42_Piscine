int	ft_atoi(char	*str)
{
	int	i;
	int	a;
	int	x;

	i = 0;
	a = 1;
	x = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			a = a * -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		x = (str[i] - 48) + (x * 10);
		i++;
	}
	return (x * a);
}
